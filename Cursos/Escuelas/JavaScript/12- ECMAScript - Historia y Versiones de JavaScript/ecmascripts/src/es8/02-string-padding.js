const string = "Hola";

// padStart: Completa un string con otro string en el inicio hasta tener un total de caracteres especificado.
console.log(string.padStart(5, "_")); // _Hola

// padEnd: completa un string con otro string en el final hasta tener un total de caracteres especificado.
console.log(string.padEnd(5, "_")); // Hola_